const valor_a = parseInt(prompt("Informe o valor A:"));
const valor_b = parseInt(prompt("Informe o valor B:"));
(let = add), menos, vezes, dividir;

add = valor_a + valor_b;
menos = valor_a - valor_b;
vezes = valor_a * valor_b;
dividir = valor_a / valor_b;

alert(
  "A soma dos numeros é igual a :" +
    add +
    " A subtração dos numeros é igual a:" +
    menos +
    " A multiplicação dos numeros é igual a: " +
    vezes +
    " A divisão dos numeros é: " +
    dividir
);
